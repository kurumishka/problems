// --------- vector operators ----------------
template<class T>
inline ostream& operator<<(ostream& os, V<T>& vec) {
    for(auto& x : vec) os << x << " ";
    return os; }

template<class T>
inline istream& operator>>(istream& is, V<T>& vec) {
    for(auto &x : vec) is >> x;
    return is; }
// ------------------------------------------

void solve() {
    int n;
    cin >> n;
    V<int> v(n);
    cin >> v;
    int m0 = 1e9, m1 = -1;
    for (auto& x : v) {
        if (x > m1) m1=x;
        if (x < m0) m0=x;
    }
    cout << m1-m0 << endl;
}

