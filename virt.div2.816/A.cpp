template<typename T> 
using V = std::vector<T>;
using namespace std;

void solve() {
    int n, m;
    cin >> n >> m;
    if (n==1 && m == 1) {
        cout << 0 << endl;
        return;
    }
    cout << max(0, n-1+m+min(n,m)-1) << endl;
}

signed main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
